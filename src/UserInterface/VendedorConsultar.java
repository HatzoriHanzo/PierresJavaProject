    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            NegociosVendedor NegVend = new NegociosVendedor();
            DefaultTableModel modelo = new DefaultTableModel();
            Vendedor vendedor = new Vendedor();
            if (jTextFieldCodigo.getText().equals("") == false) {
                vendedor.setVend_codigo(Integer.parseInt(jTextFieldCodigo.getText()));
            }
            if (jTextFieldCpf.getText().equals("") == false) {
                vendedor.setVendedor_cpf(jTextFieldCpf.getText());
            }
            if (jTextFieldNome.getText().equals("") == false) {
            vendedor.setVendedor_nome(jTextFieldNome.getText());
            }
            ArrayList<Vendedor> vendedores = NegVend.consultar(vendedor);

            modelo.setColumnIdentifiers(new Object[]{"C贸digo", "Nome", "Cpf"});

            for (Vendedor v : vendedores) {
                modelo.addRow(new Object[]{v.getVend_codigo(), v.getVendedor_nome(), v.getVendedor_cpf()});
            }

            jTable1.setModel(modelo);

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

